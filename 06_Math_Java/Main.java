// Math class in java allow us to perform mathematical operation on mumbers.


public class Main {
    public static void main(String[] args) {
        System.out.println(Math.min(3, 5)); // return smaller number
        System.out.println(Math.max(3, 5)); // return largest number
        System.out.println(Math.pow(2, 3)); // return the value of a to the power b
        System.out.println(Math.sqrt(2)); // return square root of a
        System.out.println(Math.abs(-3.45)); // return the positive value of a
        System.out.println(Math.random()); // return the numbers between 0.00 to 1.0
        System.out.println(Math.random()*100); // return the numbers between 1.000 to 100.000
    }
}
