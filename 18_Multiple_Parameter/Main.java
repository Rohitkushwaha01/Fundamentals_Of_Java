// Multiple Parameters : Here we have used two parameters one is fname and another is lname.


public class Main {
    static void myMethod(String fname, String lname){
        System.out.println(fname + " " + lname);
    }
    public static void main(String[] args) {
        myMethod("Rohit", "Kushwaha");
        myMethod("Shubham", "Bodkhe");
        myMethod("Gaurav", "Datir");
    }
}
