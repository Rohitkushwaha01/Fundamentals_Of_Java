public class Main{
    public static void main(String[] args) {
        System.out.println(5&7); // Bitwise And operator
        System.out.println(5|8); // Bitwise OR operator
        System.out.println(5^7); // Bitwise XOR operator
        System.out.println(~0); // Bitwise NOT operator
        System.out.println(5<<2); // Bitwise left shift operator
        System.out.println(10>>1); // Bitwise right shift operator
    }
}